// title: combinations of size N from an array
// should be permutations of size N from an array

const xarray = ['19', '21', '42', '23', '25', '28'],
  n = 4;

interface Input {
  a: string[];
  n: number;
  s: string[];
  t: string[];
}

function getPermutations(
  a: string[],
  n: number,
  s: string[] = [],
  t: string[] = []
) {
  return a.reduce((p, c, i, a) => {
    n > 1
      ? getPermutations(
          a.slice(0, i).concat(a.slice(i + 1)),
          n - 1,
          p,
          (t.push(c), t)
        )
      : // @ts-ignore
        p.push((t.push(c), t).slice(0));
    t.pop();
    return p;
  }, s);
}
document.write(
  '<pre>' + JSON.stringify(getPermutations(xarray, n), null, 2) + '</pre>'
);

